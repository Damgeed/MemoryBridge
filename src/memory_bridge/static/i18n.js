/**
 * Apply the current locale to all elements with data-i18n attributes.
 * Falls back to English if the key is missing for the current language.
 */
function applyTranslations() {
  const lang = localStorage.getItem('mb-lang') || 'en';
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    let text = t[key] || TRANSLATIONS.en[key];
    if (text) {
      el.innerHTML = text;
    }
  });

  // Also set html lang attribute
  document.documentElement.lang = lang;
}

/**
 * Initialize language from localStorage on page load.
 */
function initLanguage() {
  const saved = localStorage.getItem('mb-lang') || 'en';
  document.querySelectorAll('.lang-opt').forEach(el => el.classList.remove('active'));
  const opt = document.querySelector(`.lang-opt[data-lang="${saved}"]`);
  if (opt) opt.classList.add('active');
  applyTranslations();
}

/**
 * Toggle language dropdown.
 */
function toggleLang() {
  document.getElementById('lang-dropdown').classList.toggle('open');
}

function toggleLangMobile() {
  document.getElementById('lang-dropdown-mobile').classList.toggle('open');
}

/**
 * Set the current language and re-translate the page.
 */
function setLang(code) {
  localStorage.setItem('mb-lang', code);
  document.querySelectorAll('.lang-opt').forEach(el => el.classList.remove('active'));
  const opt = document.querySelector(`.lang-opt[data-lang="${code}"]`);
  if (opt) opt.classList.add('active');
  applyTranslations();
  document.getElementById('lang-dropdown')?.classList.remove('open');
  document.getElementById('lang-dropdown-mobile')?.classList.remove('open');
}

// Close language drop-downs on outside click
document.addEventListener('DOMContentLoaded', () => {
  document.addEventListener('click', (e) => {
    if (!e.target.closest('.lang-toggle') && !e.target.closest('#lang-dropdown')) {
      document.getElementById('lang-dropdown')?.classList.remove('open');
    }
    if (!e.target.closest('#lang-dropdown-mobile') && !e.target.closest('[onclick="toggleLangMobile()"]')) {
      document.getElementById('lang-dropdown-mobile')?.classList.remove('open');
    }
  });
});
